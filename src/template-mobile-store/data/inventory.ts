export type VehicleListing = {
  id: string;
  title: string;
  category: string;
  propertyType: string;
  listingType: "new" | "used" | "cpo";
  status: "available" | "sold" | "pending" | "reserved";
  featured?: boolean;
  badges?: ("featured" | "new" | "cpo" | "sale")[];
  make: string;
  model: string;
  year: string;
  trim: string;
  mileage: number;
  vin: string;
  stockNumber: string;
  fuelType: string;
  transmission: string;
  drivetrain: string;
  exteriorColor: string;
  interiorColor: string;
  engine: string;
  serviceId: string;
  location: string;
  address: string;
  city: string;
  price: number;
  msrp: number;
  priceLabel: string;
  monthlyEstimate: number;
  client: string;
  value: string;
  description: string;
  image: string;
  gallery: string[];
  agentId: string;
  amenities: string[];
  features: string[];
  options: string[];
  videoUrl: string;
  virtualTourUrl: string;
  warranty: string;
  number: number;
  lat: number;
  lng: number;
};

function vehicle(p: Omit<VehicleListing, "title" | "value" | "priceLabel" | "client"> & { title?: string }): VehicleListing {
  const title = p.title ?? `${p.year} ${p.make} ${p.model} ${p.trim}`;
  return {
    ...p,
    title,
    client: p.make,
    value: `$${p.price.toLocaleString()}`,
    priceLabel: `$${p.price.toLocaleString()}`,
  };
}

