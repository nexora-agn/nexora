function product(
  p: Omit<PhoneProduct, "title" | "value" | "priceLabel" | "client" | "mileage" | "vin" | "fuelType" | "transmission" | "drivetrain" | "exteriorColor" | "interiorColor" | "engine" | "trim" | "year"> & {
    title?: string;
    storage: string;
    model: string;
    brand: string;
  },
): PhoneProduct {
  const title = p.title ?? `${p.brand} ${p.model} ${p.storage}`;
  return {
    ...p,
    title,
    trim: p.storage,
    year: "2025",
    mileage: 0,
    vin: "",
    fuelType: p.connectivity,
    transmission: p.ram,
    drivetrain: p.processor,
    exteriorColor: p.color,
    interiorColor: p.color,
    engine: p.processor,
    client: p.brand,
    value: `$${p.price.toLocaleString()}`,
    priceLabel: `$${p.price.toLocaleString()}`,
  };
}

